    private void prepararComboDisciplina() {
        int id = Session.getInstance().getUsuario().getIdUsuario();

        for (Disciplina disc : new ProfessorDisciplinaDAO().ListarDisciplinaDeProfessor(id)) {
            comboBoxDisc.addItem(disc);

        }
        prepararComboAssunto();
    }

    private void prepararComboAssunto() {
        comboBoxAssunto.removeAllItems();
        Disciplina disc = (Disciplina) comboBoxDisc.getSelectedItem();
        List<Assunto> assuntos = new AssuntoDAO().listarAssuntoByIdDisciplina(disc.getId());
        for (Assunto as : assuntos) {
            comboBoxAssunto.addItem(as);
        }

    }

    private int getSelected() {
        int selected = -1;
        //retorna 1 caso seja descritiva, 2 caso seja multipla escolha 

        if (GrupoTipo.isSelected(BTDescritiva.getModel())) {

            selected = 2;

        }
        if (GrupoTipo.isSelected(BTitens.getModel())) {
            selected = 1;
        }
        //System.out.println(selected);
        return selected;
    }

