    public void consultarDisciplina(){
        ProfessorDisciplinaDAO profDisc = new ProfessorDisciplinaDAO();
        int id = Session.getInstance().getUsuario().getIdUsuario();
        for(Disciplina disc : profDisc.ListarDisciplinaDeProfessor(id)){
            CBDisc.addItem(disc);
        }
    }

