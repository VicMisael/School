    private void JBEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBEditarActionPerformed
        // ao clicar na linha da tabela editar o campo]
        if (JTDisciplina.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "selecione uma disciplina", "Sistema de Provas", JOptionPane.ERROR_MESSAGE);
        } else {
            int id = (int) JTDisciplina.getModel().getValueAt(JTDisciplina.getSelectedRow(), 0);
            String nomeatual = (String) JTDisciplina.getModel().getValueAt(JTDisciplina.getSelectedRow(), 1);
            AlterarDisciplina alt = new AlterarDisciplina(id, nomeatual);
            Principal.Principal.Desktop.add(alt);
            alt.show();
        }


    }//GEN-LAST:event_JBEditarActionPerformed

    private void JBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBExcluirActionPerformed
        // ao clicar na linha da tabela excluir o campo
        int linha = JTDisciplina.getSelectedRow();
        if (linha != -1) {
            Disciplina disc = new Disciplina();
            DisciplinaDAO discDAO = new DisciplinaDAO();
            disc.setId((int) JTDisciplina.getValueAt(linha, 0));

            int resposta;
            resposta = JOptionPane.showConfirmDialog(null, "Deseja excluir " + JTDisciplina.getValueAt(linha, 1) + "?");
            if (resposta == JOptionPane.YES_OPTION) {
                // verifica se o usuário clicou no botão YES
                discDAO.DeleteDisciplina(disc);
            }
            ListaTable();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma disciplina primeiro", "Sistema de Prova", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_JBExcluirActionPerformed

