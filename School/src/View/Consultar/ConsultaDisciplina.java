    public void ListaTable() {
        DefaultTableModel modelo = (DefaultTableModel) JTDisciplina.getModel();
        DisciplinaDAO discDAO = new DisciplinaDAO();
        modelo.setNumRows(0);
        for (Disciplina disc : discDAO.listarDisciplina()) {
            modelo.addRow(new Object[]{
                disc.getId(),
                disc.getDisciplina()
            });
        }

    }

    public void ListaTable(String pesquisa) {
        DefaultTableModel modelo = (DefaultTableModel) JTDisciplina.getModel();
        DisciplinaDAO discDAO = new DisciplinaDAO();
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);

        }

        for (Disciplina disc : discDAO.ListarDisciplinaPorNome(pesquisa)) {
            modelo.addRow(new Object[]{
                disc.getId(),
                disc.getDisciplina()
            });
        }

    }

