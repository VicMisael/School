    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        // TODO add your handling code here:
        if (TabelaAssunto.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Selecione uma linha", title, JOptionPane.INFORMATION_MESSAGE);
        } else {
            int id = (int) TabelaAssunto.getModel().getValueAt(TabelaAssunto.getSelectedRow(), 0);
            new AssuntoDAO().deleteAssunto(id);
            ListaTable();
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (TabelaAssunto.getSelectedRow() != -1) {
            int id = (int) TabelaAssunto.getModel().getValueAt(TabelaAssunto.getSelectedRow(), 0);
            Assunto as = new AssuntoDAO().getAssuntoByAssuntoId(id);
            AlterarAssunto alteraassunto = new AlterarAssunto(as);
            Principal.Desktop.add(alteraassunto);
            alteraassunto.show();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha", title, JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

