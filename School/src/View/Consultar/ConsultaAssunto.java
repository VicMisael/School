    public void ListaTable() {
        DefaultTableModel modelo = (DefaultTableModel) TabelaAssunto.getModel();

        modelo.setNumRows(0);
        AssuntoDAO assunto = new AssuntoDAO();
        int id = Session.getInstance().getUsuario().getIdUsuario();
        for (Assunto as : assunto.listarAssunto(id)) {
            modelo.addRow(new Object[]{
                as.getIdAssunto(),
                as.getNome(),
                as.getNomeDisciplina()

            });
        }
    }

    public void ListaTable(String assuntoNome, String disciplinaNome) {
        DefaultTableModel modelo = (DefaultTableModel) TabelaAssunto.getModel();

        modelo.setNumRows(0);
        AssuntoDAO assunto = new AssuntoDAO();
        int id = Session.getInstance().getUsuario().getIdUsuario();
        for (Assunto as : assunto.listarAssunto(id, assuntoNome, disciplinaNome)) {
            modelo.addRow(new Object[]{
                as.getIdAssunto(),
                as.getNome(),
                as.getNomeDisciplina()

            });
        }
    }

